#include <iostream>
#include "Contact.h"

Contact::Contact(int id, int nr,string nme, string snme, string num, string ml, string addrs)
{

    contactOwnerId=id;  //owner id
    entryNum=nr;        // No. of position 1,2,3...n
    name=nme;           //rest of data
    surname=snme;
    number=num;
    mail=ml;
    address=addrs;      //eof.

}
